# função de soma
def somar(a,b):
    return float(a) + float(b)

# função de subtração
def subtrair(a,b):
    return float(a) - float(b)

# função de divisão
def dividir(a,b):
    if b == 0:
        return 0
    else:
        return float(a) / float(b)

# função de multiplicação
def multiplicar(a,b):
    return float(a) * float(b)


