subtracao = valor1 - valor2
print("A subtracao é {}".format(subtracao))

divisao = valor1 / valor2
print("A divisao é {:.2f}".format(divisao))

multiplicacao = valor1 * valor2
print("A multiplicacao é {}".format(multiplicacao))




